class Solution:
    def shortestBridge(self, grid: List[List[int]]) -> int:
        island_1 = set()

        for i in range(len(grid)):
            for j in range(len(grid[0])):
                if (grid[i][j] == 1) and (len(island_1) == 0):
                    dfs(grid, i, j, island_1, -1)

        distance_to_other_island = bfs(grid, island_1, 1)

        return distance_to_other_island


def dfs(grid, i, j, accumulator, identifier):
    if i >= len(grid) or j >= len(grid[0]) or i < 0 or j < 0:
        return

    if grid[i][j] != 1:
        return

    grid[i][j] = identifier
    accumulator.add((i, j))

    dfs(grid, i + 1, j, accumulator, identifier)
    dfs(grid, i - 1, j, accumulator, identifier)
    dfs(grid, i, j + 1, accumulator, identifier)
    dfs(grid, i, j - 1, accumulator, identifier)


def bfs(grid, island, identifier_to_look_for):
    queue = collections.deque()
    visited = set()

    for (i, j) in island:
        queue.append((i, j, 0))

    while len(queue) != 0:
        (x, y, distance) = queue.popleft()

        if x >= len(grid) or y >= len(grid[0]) or x < 0 or y < 0:
            continue

        if (x, y) in visited:
            continue

        if grid[x][y] == identifier_to_look_for:
            return distance - 1

        # if grid[x][y] != 0:
        #     continue

        visited.add((x, y))

        queue.append((x, y + 1, distance + 1))
        queue.append((x, y - 1, distance + 1))
        queue.append((x + 1, y, distance + 1))
        queue.append((x - 1, y, distance + 1))

    return math.inf


